package net.mcreator.workspace.elements;

import net.mcreator.ui.dialogs.GeneralTextureSelector;

public class TextureElement {
	private String name;
	private String path;
	private String folderName;
	private GeneralTextureSelector.TextureType type;

	public TextureElement(String name, String path, String folderName, GeneralTextureSelector.TextureType type) {
		this.name = name;
		this.path = path;
		this.folderName = folderName;
		this.type = type;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public String getFolderName() {
		return folderName;
	}

	public void setFolderName(String folderName) {
		this.folderName = folderName;
	}

	public GeneralTextureSelector.TextureType getType() {
		return type;
	}

	public void setType(GeneralTextureSelector.TextureType type) {
		this.type = type;
	}
}
